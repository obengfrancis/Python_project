#Program to calculate students grade
print ("--------------------------------------------")
Grade = int(input("Enter your score: "))
if Grade >= 90:
    print("Grade 'A'")
elif Grade >= 80:
    print("Grade 'B'")
elif Grade >= 70:
    print("Grade 'C'")
elif Grade >= 60:
    print("Grade 'D'")
else:
    print("Grade 'F'")
