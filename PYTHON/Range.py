print("Program using for loop range")
print('-------------------------------')

for x in range(10):
    print(x)
    

print('-------------------------------')
for x in range(0, 10):
    print(x)


print('-------------------------------')
for x in range(1, 7):
    print(x)


print('-------------------------------')
for x in range(0, 30, 7):
    print(x)
